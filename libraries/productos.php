/* catalogo por categoria*/
function catalogo_x_categoria(string $producto) : array {
    $resultado = [];
    $catalogo = catalogo_completo();
    foreach ($catalogo as $p ) {
        if ($p["tipo"] == $producto) {
            $resultado[] = $p;
        }
    }
    return $resultado;
}

/* Producto en particular */
function producto_x_id(int $idProducto){
    $catalogo = catalogo_completo();

    foreach ($catalogo as $p) {
         if($p['id'] == $idProducto){
               return $p;
       }
    }

    return null;

}

/* Todos los productos */
function catalogo_todos_productos() : array {
    $resultado = [];
    $catalogo = catalogo_completo();

    foreach ($catalogo as $p) {
        $resultado[] = $p;
    }

    return $resultado;

}


/* Productos Destacados */

function producto_destacado() : array {
    $resultado = [];
    $catalogo = catalogo_completo();

    foreach ($catalogo as $p) {
        if($p['destacado'] ?? false){
            $resultado[] = $p;
    }
    }

    return $resultado;

}
